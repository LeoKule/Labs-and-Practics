#include <iostream>

int main()
{
  // Исходный массив
  int array1[] = {1, 2, 3, 4, 5};
  const int n = sizeof(array1) / sizeof(array1[0]);

  // Создаем новый массив такой же длины, как и исходный
  int array2[n];

  // Копируем содержимое массива 1 в массив 2, используя указатели
  int *srcPtr = array1;
  int *dstPtr = array2;

  for (int i = 0; i < n; ++i) {
    *dstPtr++ = *srcPtr++;
  }

  // Выводим содержимое массива 2 для проверки
  for (int i = 0; i < n; ++i) {
    std::cout << array2[i] << " ";
  }

  return 0;
}
//Здесь мы используем указатели srcPtr и dstPtr для копирования содержимого
//массива 1 в массив 2. Мы проходим по всем элементам массива 1, копируя их в массив массива 1 в массив 2. Мы проходим по всем элементам массива 1, копируя их в массив 2
// используя оператор разыменования указателей *. Такой способ копирования
// может оказаться более быстрым, чем использование стандартной функции std::copy,
// так как он не вызывает никаких функций и не создает дополнительных объектов.